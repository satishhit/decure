    /**
     * Create new page and assign to PDF object
     *
     * @param  array $settings
     * @return \Zend_Pdf_Page
     */
	protected function insertSignature(&$page)
    {
		$objectManager = \Magento\Framework\App\ObjectManager::getInstance();
		$helperData = $objectManager->create('\Magecomp\Gstcharge\Helper\Data');

		$this->y = $this->y ? $this->y : 815;
        $image = $helperData->getAuthenticationSignature();
		
        if ($image) {
			
			$fileSystem = $objectManager->create('\Magento\Framework\Filesystem');
            $image = $fileSystem->getDirectoryRead(\Magento\Framework\App\Filesystem\DirectoryList::MEDIA)->getAbsolutePath("gst/".$helperData->getAuthenticationSignature());
            if (is_file($image)) {
               
			 
			
			$mediaPath=$fileSystem->getDirectoryRead(\Magento\Framework\App\Filesystem\DirectoryList::MEDIA)->getAbsolutePath("gst/".$helperData->getAuthenticationSignature());
			$image =  \Zend_Pdf_Image::imageWithPath($mediaPath);

                $top         =  $this->y - 250; //top border of the page
                $widthLimit  = 100; //half of the page width
                $heightLimit = 100; //assuming the image is not a "skyscraper"
                $width       = $image->getPixelWidth();
                $height      = $image->getPixelHeight();

                //preserving aspect ratio (proportions)
                $ratio = $width / $height;
                if ($ratio > 1 && $width > $widthLimit) {
                    $width  = $widthLimit;
                    $height = $width / $ratio;
                } elseif ($ratio < 1 && $height > $heightLimit) {
                    $height = $heightLimit;
                    $width  = $height * $ratio;
                } elseif ($ratio == 1 && $height > $heightLimit) {
                    $height = $heightLimit;
                    $width  = $widthLimit;
                }

                $y1 = $top - $height;
                $y2 = $top;
                $x1 = 450;
                $x2 = $x1 + $width;
                
				$imgY = $this->y - $height - 60 ; 
				
				
				$page->drawText(__($helperData->getAuthenticationSignatureText()) ,$x1, $this->y - 250 ,'UTF-8');
				$page->drawImage($image, $x1, $y1, $x2, $y2-10);
				$page->drawText(__('Authorized Signatory') ,$x1, $this->y - 300 ,'UTF-8');

                $this->y = $y1 - 10;
           }
        }
    }
    public function newPage(array $settings = [])
    {
        /* Add new table head */
        $page = $this->_getPdf()->newPage(\Zend_Pdf_Page::SIZE_A4);
        $this->_getPdf()->pages[] = $page;
        $this->y = 800;
        if (!empty($settings['table_header'])) {
            $this->_drawHeader($page);
        }
        return $page;
    }
	
